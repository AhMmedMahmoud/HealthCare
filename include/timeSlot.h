#ifndef TIME_SLOT_H
#define TIME_SLOT_H

// Enum to represent different time slots  
enum TimeSlot {  
    SLOT_0,
    SLOT_1, // 9:00 AM - 10:00 AM  
    SLOT_2, // 10:00 AM - 11:00 AM  
    SLOT_3, // 11:00 AM - 12:00 PM  
    SLOT_4, // 1:00 PM - 2:00 PM  
    SLOT_5, // 2:00 PM - 3:00 PM  
    SLOT_6  // 3:00 PM - 4:00 PM  
};

#endif

